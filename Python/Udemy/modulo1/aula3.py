"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas.
"""

print (1234)

# Aspas simples

print ('Luiz Otávio')
print ('Luiz "Otávio"')

# Aspas duplas

print ("Fábio Henrique")
print ("Fábio 'Henrique'")

# Escape (essa parte é incrível, nunca fiz dessa forma)

print("Fábio \"Henrique\"")

# r (O r torna possível visualizar os símbolos de escape [\])
print(r"Luiz \"Otávio\"")

